function entryForKey(key: string): TranslationEntry {
  const fromOverrides = overrides[key];
  if (fromOverrides) return fromOverrides;
  const text = keyToStubText(key);
  return { en: text, hi: text };
}

/**
 * Dictionary used by `LanguageProvider`: `tr(key)` reads `t[key][lang]`.
 * Implemented as a Proxy so every key resolves without maintaining a huge static map.
 */
export const t: Record<string, TranslationEntry> = new Proxy(
  {} as Record<string, TranslationEntry>,
  {
    get(_target, prop: string | symbol) {
      if (typeof prop !== "string") return undefined;
      return entryForKey(prop);
    },
    has(_target, prop) {
      return typeof prop === "string";
    },
  },
);
